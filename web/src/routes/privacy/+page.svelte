<h1>Privacy</h1>

<h2>Personal information</h2>
<p>
    The only personal information ApriBot stores about you is your Reddit
    username (on posts which you have labelled). This information is required to
    make sure that you are not asked to label the same post twice. It is not
    shared with anybody else, and your votes can only be viewed by you.
</p>

<h2>Cookies</h2>
<p>
    ApriBot uses one cookie to log you into Reddit securely (using OAuth), and
    another to store your login status so that you don't have to keep logging
    in, but otherwise does not perform any other tracking. If you want to verify
    this, the source code of ApriBot can be viewed on <a
        href="https://github.com/penelopeysm/apribot">GitHub</a
    >.
</p>
